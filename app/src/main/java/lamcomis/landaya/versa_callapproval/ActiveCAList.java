package lamcomis.landaya.versa_callapproval;

public class ActiveCAList {
    String ca_no;
    String ca_date;
    String customer;
    String purpose;
    String employee_name;
    String job_des;
    public String getCa_no() {
        return ca_no;
    }

    public void setCa_no(String ca_no) {
        this.ca_no = ca_no;
    }

    public String getCa_date() {
        return ca_date;
    }

    public void setCa_date(String ca_date) {
        this.ca_date = ca_date;
    }

    public String getCustomer() {
        return customer;
    }

    public void setCustomer(String customer) {
        this.customer = customer;
    }

    public String getPurpose() {
        return purpose;
    }

    public void setPurpose(String purpose) {
        this.purpose = purpose;
    }

    public String getEmployee_name() {
        return employee_name;
    }

    public void setEmployee_name(String employee_name) {
        this.employee_name = employee_name;
    }

    public String getJob_des() {
        return job_des;
    }

    public void setJob_des(String job_des) {
        this.job_des = job_des;
    }


}
